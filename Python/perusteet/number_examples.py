# number_examples.py
# Numerot ja laskutoimitukset

luku1 = 10
luku2 = 3

print("Summa:", luku1 + luku2)           # Yhteenlasku: 13
print("Erotus:", luku1 - luku2)          # Vähennyslasku: 7
print("Tulo:", luku1 * luku2)             # Kertolasku: 30
print("Osamäärä:", luku1 / luku2)         # Jakolasku: 3.3333...
print("Kokonaisluku jako:", luku1 // luku2) # Kokonaisjakolasku: 3
print("Jakojäännös:", luku1 % luku2)      # Jakojäännös: 1
print("Potenssi:", luku1 ** luku2)        # Potenssi: 1000
